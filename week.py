num1 = int(input("Enter a number between 1 and 7: "))

if num1 == 1:
    print("Sunday")
elif num1 == 2:
    print("Monday")
elif num1 == 3:
    print("Tuesday")
elif num1 == 4:
    print("Wednesday")
elif num1 == 5:
    print("Thursday")
elif num1 == 6:
    print("Friday")
elif num1 == 7:
    print("Saturday")
else:
    print("there's no day associated with this number")
